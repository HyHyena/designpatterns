package CommandPattern;

public class Light {

    public void on(){
        System.out.println("Turning light on");
    }

    public void off(){
        System.out.println("Turning light off");
    }

    public void dimDown(){
        System.out.println("Dimming light down");
    }

    public void dimUp(){
        System.out.println("Dimming light up");
    }
}
